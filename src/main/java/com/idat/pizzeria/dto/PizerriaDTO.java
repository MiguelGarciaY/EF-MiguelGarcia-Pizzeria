package com.idat.pizzeria.dto;

public class PizerriaDTO {
	private Integer id;
	private String dirPizerria;
	private String sedePizerria;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDirPizerria() {
		return dirPizerria;
	}
	public void setDirPizerria(String dirPizerria) {
		this.dirPizerria = dirPizerria;
	}
	public String getSedePizerria() {
		return sedePizerria;
	}
	public void setSedePizerria(String sedePizerria) {
		this.sedePizerria = sedePizerria;
	}
}
